/**
 * A class that contains static values, which represents
 * the different sizes of the different data units (in bytes).
 */
export default class DataSizes {
    static NONE = 0;
    static UINT8 = 1;
    static INT8 = 1;
    static UINT16 = 2;
    static INT16 = 2;
    static UINT32 = 4;
    static INT32 = 4;
    static UINT64 = 8;
    static INT64 = 8;
    static FLOAT = 4;
    static DOUBLE = 8;
}